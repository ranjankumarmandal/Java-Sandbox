public class VariablesTutorial {
    public static void main(String[] args) {
        // Syntax is like this
        // data_type variable_name = value

        boolean b = true;
        byte by = 100;
        short s = 1000;
        int i = 100000;
        long l = 123454321;
        double d = 12345.678;
        char ch = 'A';

        int a = (int)123.456; // Explicit casting
        double c = 1456; // Implicit casting
        System.out.println("The value of c is " + c);
    }
}